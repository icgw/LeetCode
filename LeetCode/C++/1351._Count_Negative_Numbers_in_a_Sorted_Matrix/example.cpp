#include <iostream>
#include "solution.h"

void run_example()
{
  Solution sln;

  vector<vector<int>> grid {
    { 4, 3, 2, -1 }, { 3, 2, 1, -1 }, { 1, 1, -1, -2 }, { -1, -1, -2, -3 }
  };

  int cnt = sln.countNegatives(grid);

  std::cout << cnt << std::endl;
}
